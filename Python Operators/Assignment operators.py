# Assignment operators
#  = ->> x = 5 ->> x = 5
#  += ->> x += 5 ->> x = x + 5
#  -= ->> x -= 5 ->> x = x - 5
#  *= ->> x +=*= 5 ->> x = x * 5
#  /= ->> x += =/5 ->> x = x / 5
#  %= ->> x %= 5 ->> x = x % 5
#  //= ->> x //= 5 ->> x = x // 5
#  **= ->> x **= 5 ->> x = x ** 5

x = 10

x = x + 5  # Output: 20
x - 5  # Output: 0
x * 5  # Output: 100
x / 5  # Output: 1
x % 5  # Output: 0
x // 5  # Output: 10000000000
x ** 5  # Output: 1
