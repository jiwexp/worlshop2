# 1.จงเขียนตารางสูตรคูณให้ผลลัพท์ที่ออกมาเป็นแบบตัวอย่างด้านล่างโดยใช้คำสั่ง while
a = 1
b = int(input(""))
while a <= 12:
    print(b, "*", a, ": ", b * a)
    a += 1

# 2.จงเขียนตารางสูตรคูณให้ผลลัพท์ที่ออกมาเป็นแบบตัวอย่างด้านล่างโดยใช้คำสั่ง for
x = int(input(""))
for y in range(1, 13):
    z = y * x
    print(x, "*", y, ": ", z)
